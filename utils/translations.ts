// ========================================================================
// Runtime translation cache (for tribal languages that aren't in static dict)
// ========================================================================
// We auto-translate the entire English dict via Gemma the first time a user
// picks a language not covered by the static dict (Bodo / Mizo / Khasi /
// Manipuri). Result is cached in localStorage so subsequent loads are instant.

const DYNAMIC_CACHE_KEY = 'kropscan_dynamic_translations';

type DynamicCache = Record<string, Record<string, string>>;

const loadDynamicCache = (): DynamicCache => {
    try {
        const raw = localStorage.getItem(DYNAMIC_CACHE_KEY);
        return raw ? JSON.parse(raw) : {};
    } catch {
        return {};
    }
};

const saveDynamicCache = (cache: DynamicCache) => {
    try {
        localStorage.setItem(DYNAMIC_CACHE_KEY, JSON.stringify(cache));
    } catch {
        /* ignore quota errors */
    }
};

const dynamicCache: DynamicCache = loadDynamicCache();

export const getTranslation = (lang: string, key: string): string => {
    // 1. Runtime-translated cache wins (covers tribal langs)
    const cached = dynamicCache[lang]?.[key];
    if (cached) return cached;
    // 2. Static dict (Hindi/Marathi/etc)
    const languageData = translations[lang] || translations['English'];
    return languageData[key] || translations['English'][key] || key;
};

export const hasStaticTranslations = (lang: string): boolean =>
    !!translations[lang] && lang !== 'English';

// Treat 100+ cached entries as "fully translated". Below that we'll re-fetch.
// The full dict is ~290 keys; a successful prefetch lands ~250-280 of them
// (some short strings get skipped in translateBatch).
export const hasDynamicTranslations = (lang: string): boolean =>
    !!dynamicCache[lang] && Object.keys(dynamicCache[lang]).length >= 100;

// Pre-warm the cache for a language by batch-translating the entire English
// dict. Parallel chunks (concurrency 5) typically finish in 30-60s, then the
// result is cached in localStorage forever. Strings shorter than 3 chars are
// skipped (translateBatch ignores them anyway) — they're typically codes/IDs.
export const prefetchLanguageDict = async (
    lang: string,
    onProgress?: (pct: number) => void
): Promise<void> => {
    if (lang === 'English') return;
    if (hasStaticTranslations(lang)) return; // static dict covers it
    if (hasDynamicTranslations(lang)) return; // already cached

    const englishKeys = translations['English'];
    const allEntries: [string, string][] = Object.entries(englishKeys)
        .filter(([, v]) => typeof v === 'string' && v.length >= 3);
    const CHUNK_SIZE = 25;

    const { translateBatch } = await import('../services/GeminiService');

    if (!dynamicCache[lang]) dynamicCache[lang] = {};

    const chunks: [string, string][][] = [];
    for (let i = 0; i < allEntries.length; i += CHUNK_SIZE) {
        chunks.push(allEntries.slice(i, i + CHUNK_SIZE));
    }

    // Fire chunks in parallel with bounded concurrency. 5-way is the sweet
    // spot — under Gemma free-tier per-minute limits and cuts wall-clock ~5x.
    const CONCURRENCY = 5;
    let done = 0;
    const runChunk = async (chunk: [string, string][]) => {
        const inputMap: Record<string, string> = {};
        chunk.forEach(([k, v]) => { inputMap[k] = v; });
        try {
            const translated = await translateBatch(inputMap, lang);
            Object.assign(dynamicCache[lang], translated);
        } catch (err) {
            console.warn(`[i18n] chunk translation failed for ${lang}:`, err);
        }
        done++;
        if (onProgress) onProgress(Math.round((done / chunks.length) * 100));
    };

    const queue = [...chunks];
    const workers: Promise<void>[] = [];
    for (let i = 0; i < CONCURRENCY; i++) {
        workers.push((async () => {
            while (queue.length > 0) {
                const next = queue.shift();
                if (next) await runChunk(next);
            }
        })());
    }
    await Promise.all(workers);

    // Retry pass: any keys still missing from the cache (translateBatch skipped
    // them because the result looked untranslated). Smaller chunks + a focused
    // prompt have better odds the second time around.
    const stillMissing: [string, string][] = allEntries.filter(
        ([k]) => !dynamicCache[lang]?.[k]
    );
    if (stillMissing.length > 0) {
        const retryChunks: [string, string][][] = [];
        const RETRY_CHUNK = 10;
        for (let i = 0; i < stillMissing.length; i += RETRY_CHUNK) {
            retryChunks.push(stillMissing.slice(i, i + RETRY_CHUNK));
        }
        const retryQueue = [...retryChunks];
        const retryWorkers: Promise<void>[] = [];
        for (let i = 0; i < CONCURRENCY; i++) {
            retryWorkers.push((async () => {
                while (retryQueue.length > 0) {
                    const next = retryQueue.shift();
                    if (next) await runChunk(next);
                }
            })());
        }
        await Promise.all(retryWorkers);
    }

    saveDynamicCache(dynamicCache);
};
